# 변수 활용
stockName = None
stockName = "삼성전자"
print("종목명 : %s" % stockName)
print("변수 타입 : %s" % type(stockName))

stockPrice = 56000
print("현재가 : %i" % stockPrice)
print("변수 타입 : %s" % type(stockPrice))

stockRate = -0.72
print("등락율 : %f" % stockRate)
print("등락율 : %f%%" % stockRate)
print("등락율 : %f%s" % (stockRate, "%"))
print("변수 타입 : %s" % type(stockRate))

mesuOk = False
print("매수여부 : %s" % mesuOk)
print("변수 타입 : %s" % type(mesuOk))